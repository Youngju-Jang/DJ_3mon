package bit.basic.obj1;

public class Member {
	private String name;
	private String id;
	private String email;
	private int countValue;
	
	public Member() {
		super();
	}
	public Member(String name, String id, String email, int countValue) {
		super();
		this.name = name;
		this.id = id;
		this.email = email;
		this.countValue = countValue;
	}	
	
	
}
